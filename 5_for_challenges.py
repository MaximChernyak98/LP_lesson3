# Задание 5
# Для каждой пары учеников нужно с новой строки перечислить учеников,
# которые в неё входят.
# Пример:
# Группа 1: Вася, Маша
# Группа 2: Оля, Петя, Гриша

groups = [
    ['Вася', 'Маша'],
    ['Оля', 'Петя', 'Гриша'],
]
for index, group in enumerate(groups):
    print(f'Группа {index+1}: {", ".join(group)}')
