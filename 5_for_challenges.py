# Задание 3
# Необходимо вывести имена всех учеников из списка, рядом с именем вывести пол ученика

is_male = {
    'Оля': False,  # если True, то пол мужской
    'Петя': True,
    'Вася': True,
    'Маша': False,
}
names = ['Оля', 'Петя', 'Вася', 'Маша']
for person in names:
    print(f'{person} - {"male" if is_male[person] else "female"}')


# Задание 4
# Даны группу учеников. Нужно вывести количество групп и для каждой группы –
# количество учеников в ней
# Пример вывода:
# Всего 2 группы.
